<h1>while Loop<h1>
<?php
$x = 1;

while($x <= 5) {
  echo "Pawan Verma <br> ";
  $x++;
}
?>

<h1>do while Loop<h1>
<?php
$x = 1;

do {
  echo "GLA University <br>";
  $x++;
} while ($x <= 5);
?>
<h1>for Loop<h1>
<?php
for ($x = 0; $x <= 10; $x++) {
  echo "Hello PHP<br>";
}
?>